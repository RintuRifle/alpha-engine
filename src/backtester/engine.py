"""
Backtesting engine — the core day-by-day simulation loop.

CRITICAL: Trades execute on signal[t+1], NOT signal[t].
This is achieved by df['signal'].shift(1) — look-ahead bias prevention.
Without this, backtests look unrealistically profitable because they use
information that wouldn't be available at trade time.
"""

import pandas as pd
import numpy as np

from src.utils.helpers import load_config
from src.utils.logger import get_logger
from .portfolio import Portfolio
from .transaction_costs import TransactionCosts
from .position_sizing import PositionSizer
from .order_manager import OrderManager

logger = get_logger(__name__)


class BacktestEngine:
    """
    Event-driven backtester that simulates trading day by day.

    Flow for each trading day:
    1. Read the PREVIOUS day's signal (shifted by 1 to prevent look-ahead bias)
    2. Execute trades at today's open price
    3. Apply transaction costs (commission + slippage)
    4. Mark-to-market at today's close price
    5. Record portfolio equity

    Usage:
        engine = BacktestEngine(data=df_with_signals, ticker="AAPL")
        portfolio = engine.run()
        equity_df = portfolio.get_equity_df()
    """

    def __init__(
        self,
        data: pd.DataFrame,
        ticker: str,
        initial_capital: float | None = None,
        commission: float | None = None,
        slippage: float | None = None,
        position_sizing_method: str = "fixed_capital",
        position_sizing_params: dict | None = None,
    ):
        """
        Args:
            data: DataFrame with OHLCV data and a 'signal' column from a strategy.
            ticker: Stock ticker being backtested.
            initial_capital: Starting cash. Defaults to config.yaml value.
            commission: Commission rate. Defaults to config.yaml value.
            slippage: Slippage rate. Defaults to config.yaml value.
            position_sizing_method: 'fixed_capital', 'fixed_shares', or 'kelly'.
            position_sizing_params: Extra params for the position sizer.
        """
        self.data = data.copy()
        if "signal" not in self.data.columns:
            raise ValueError("Data must contain a 'signal' column generated by a strategy.")

        self.ticker = ticker

        # Load defaults from config, allow overrides
        try:
            config = load_config()
            trading = config.get("trading", {})
            sizing_config = config.get("position_sizing", {})
        except Exception:
            trading = {}
            sizing_config = {}

        capital = initial_capital or trading.get("initial_capital", 10000.0)
        comm = commission if commission is not None else trading.get("commission", 0.001)
        slip = slippage if slippage is not None else trading.get("slippage", 0.0005)

        self.portfolio = Portfolio(capital)
        self.tc = TransactionCosts(comm, slip)
        self.order_manager = OrderManager(self.portfolio, self.tc)

        sizing_method = position_sizing_method or sizing_config.get("method", "fixed_capital")
        sizing_params = position_sizing_params or {"allocation": sizing_config.get("allocation", 0.10)}
        self.sizer = PositionSizer(sizing_method, **sizing_params)

    def run(self) -> Portfolio:
        """
        Execute the full backtest simulation.

        Returns:
            Portfolio object containing equity curve, trade history, and final state.
        """
        logger.info(f"{'='*60}")
        logger.info(f"Starting backtest: {self.ticker}")
        logger.info(f"  Capital: ${self.portfolio.initial_capital:,.2f}")
        logger.info(f"  Data points: {len(self.data)}")
        logger.info(f"{'='*60}")

        # Sort by date and ensure we have clean data
        self.data = self.data.sort_values("date").reset_index(drop=True)

        # ╔══════════════════════════════════════════════════════════╗
        # ║ CRITICAL: shift(1) prevents look-ahead bias!           ║
        # ║ Signal generated at day T → trade executes at day T+1  ║
        # ╚══════════════════════════════════════════════════════════╝
        self.data["trade_signal"] = self.data["signal"].shift(1).fillna(0)

        # Day-by-day simulation
        for _, row in self.data.iterrows():
            date = row["date"]
            open_price = row["open"]
            close_price = row["close"]
            signal = row["trade_signal"]

            current_position = self.portfolio.positions.get(self.ticker, 0)

            # Execute trades based on yesterday's signal, at today's open
            if signal == 1 and current_position == 0:
                # BUY signal — enter position
                qty = self.sizer.get_quantity(open_price, self.portfolio.cash)
                if qty > 0:
                    self.order_manager.execute_trade(date, self.ticker, "BUY", qty, open_price)

            elif signal == -1 and current_position > 0:
                # SELL signal — exit position
                self.order_manager.execute_trade(
                    date, self.ticker, "SELL", current_position, open_price
                )

            # End-of-day mark-to-market
            self.portfolio.update_equity(date, {self.ticker: close_price})

        # Log summary
        self._log_summary()
        return self.portfolio

    def _log_summary(self) -> None:
        """Log a summary of the backtest results."""
        equity_df = self.portfolio.get_equity_df()
        if equity_df.empty:
            return

        start_equity = equity_df["total_equity"].iloc[0]
        end_equity = equity_df["total_equity"].iloc[-1]
        total_return = (end_equity / start_equity - 1) * 100
        num_trades = len(self.portfolio.trade_history)

        logger.info(f"{'='*60}")
        logger.info(f"Backtest Complete: {self.ticker}")
        logger.info(f"  Start equity: ${start_equity:,.2f}")
        logger.info(f"  End equity:   ${end_equity:,.2f}")
        logger.info(f"  Total return: {total_return:+.2f}%")
        logger.info(f"  Total trades: {num_trades}")
        logger.info(f"{'='*60}")
